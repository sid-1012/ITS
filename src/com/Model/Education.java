package com.Model;

public class Education {

	private int educationID;
	private float secondary;
	private float higherSecondary;
	private String stream;
	private float diploma;
	private float graduation;
	private String universityUG;
	private float postGraduation;
	private String universityPG;
	private int applicantID;
	
	
	public int getEducationID() {
		return educationID;
	}
	public void setEducationID(int educationID) {
		this.educationID = educationID;
	}
	public float getSecondary() {
		return secondary;
	}
	public void setSecondary(float secondary) {
		this.secondary = secondary;
	}
	public float getHigherSecondary() {
		return higherSecondary;
	}
	public void setHigherSecondary(float higherSecondary) {
		this.higherSecondary = higherSecondary;
	}
	public String getStream() {
		return stream;
	}
	public void setStream(String stream) {
		this.stream = stream;
	}
	public float getDiploma() {
		return diploma;
	}
	public void setDiploma(float diploma) {
		this.diploma = diploma;
	}
	public float getGraduation() {
		return graduation;
	}
	public void setGraduation(float graduation) {
		this.graduation = graduation;
	}
	public String getUniversityUG() {
		return universityUG;
	}
	public void setUniversityUG(String universityUG) {
		this.universityUG = universityUG;
	}
	public float getPostGraduation() {
		return postGraduation;
	}
	public void setPostGraduation(float postGraduation) {
		this.postGraduation = postGraduation;
	}
	public String getUniversityPG() {
		return universityPG;
	}
	public void setUniversityPG(String universityPG) {
		this.universityPG = universityPG;
	}
	public int getApplicantID() {
		return applicantID;
	}
	public void setApplicantID(int applicantID) {
		this.applicantID = applicantID;
	}
	
	
	
	
}
